using DungeonCrawler.Objects;

namespace DungeonCrawler.Engine;

public class Game
{
    private Engine engine;
    Player player;

    readonly List<Wall> walls = new();
    readonly List<List<string>> level = new() {
        new(){"W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W"},
        new(){"W"," ","W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," ","W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," ","W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," ","W","W","W","W","W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," "," "," "," "," ","W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," "," "," "," "," ","W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," "," "," "," "," ","W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," ","W","W","W","W","W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W"," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," "," ","W"},
        new(){"W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W","W"}
    };

    public Game(Engine engine)
    {
        this.engine = engine;
        player = new Player(new Vector2(100, 100));
        for (int y = 0; y < level.Count; y++)
        {
            for (int x = 0; x < level[y].Count; x++)
            {
                if (level[y][x] == "W")
                {
                    Wall tempWall = new Wall()
                    {
                        Position = new Vector2(x * 32, y * 32)
                    };
                    walls.Add(tempWall);
                }
            }
        }
    }

    public void Update()
    {
        player.Update();
    }

    public void Draw()
    {
        foreach (Wall wall in walls)
        {
            wall.Draw();
        }
        player.Draw();
    }
}
